const DICTS: Record<Locale, Dict> = { fr, en };

export function t(locale: Locale, key: keyof typeof fr): string {
  return DICTS[locale][key] ?? DICTS[DEFAULT_LOCALE][key] ?? String(key);
}

/** Build a bound translator: `const tr = translator(locale); tr('nav.dashboard')`. */
export function translator(locale: Locale) {
  return (key: keyof typeof fr): string => t(locale, key);
}

